import sys, time, random, keyboard
print("press 'y' to play")
print("Balance?")
bal = int(input())
print("Wager?")
number = int(input())
#string length 300
animation = "########################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################################"
while True:
    round = random.randint(0,300)
    for i in range(len(animation)):
        if i >= round:
            print("\nYou lost: $" + str(number))
            bal -= number
            print("Balance: $" + str(bal))
            break
        if keyboard.is_pressed('y'):
            print("\nResult: $" + str("{:.2f}".format(((i/76.5)*number) - number)) )
            bal += ((i/76.5)*number) - number
            print("Balance: $" + str("{:.2f}".format(bal)))
            break
        print(animation[i], end='', flush=True)
        time.sleep(.05)
    if bal < number:
        print("Can't gamble money you don't have :)")
        break
    print("Again? Y or N")
    resp = input().lower()
    if resp == 'n':
        break
